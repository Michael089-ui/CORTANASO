    public void cargartabla(){
        
        DefaultTableModel tablaa=new DefaultTableModel();
        tablaa.addColumn("NÃšMERO FACTURA");
        tablaa.addColumn("FECHA");
        tablaa.addColumn("FORMAPAGO");
        tablaa.addColumn("VALOR TOTAL");
        tablaa.addColumn("DOCUMENTO");
        tablaa.addColumn("NOMBRE");
        tablaa.addColumn("CELULAR");
        
        tablafactura.setModel(tablaa);
        
        String arr[]=new String[10];
        
        try {
            
            ps=cnn.prepareStatement("SELECT * FROM factura_venta");
            rs=ps.executeQuery();
            
            while(rs.next()){
                
                arr[0]=rs.getString(1);
                arr[1]=rs.getString(2);
                arr[2]=rs.getString(3);
                arr[3]=rs.getString(4);
                arr[4]=rs.getString(5);
                arr[5]=rs.getString(6);
                arr[6]=rs.getString(7);
                
                tablaa.addRow(arr);

            }
            
        } 
        
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Error en la consulta" + ex);
            
        }
        
    }
    
    public void listadatos(){
        
        int lista=tablafactura.getSelectedRow();
        
        if (lista >= 0) {
            
            codigo.setText(tablafactura.getValueAt(lista, 0).toString());
            fecha.setText(tablafactura.getValueAt(lista, 1).toString());
            pago.setSelectedItem(tablafactura.getValueAt(lista, 2).toString());
            valor.setText(tablafactura.getValueAt(lista, 3).toString());
            documento.setSelectedItem(tablafactura.getValueAt(lista, 4).toString());
            nombre.setText(tablafactura.getValueAt(lista, 5).toString());
            celular.setText(tablafactura.getValueAt(lista, 6).toString());
            
        }

    }
    
    public void cargarcombo(){
        
        try {
            
            ps=cnn.prepareStatement("SELECT documentoc FROM cliente");
            rs=ps.executeQuery();  
            
            while (rs.next()){
            
                String aux=rs.getString("documentoc");
                this.documento.addItem(aux);
            
            }
            
        } 
        
        catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(null, "Error en cargar el combo"+ ex);
            
        }
        
    }
    

