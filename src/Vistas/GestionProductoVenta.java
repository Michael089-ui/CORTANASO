    public void cargartabla(){
        
        DefaultTableModel tabla=new DefaultTableModel();
        tabla.addColumn("NÃšMERO DE SERIE");
        tabla.addColumn("DISPONIBILIDAD");
        
        tablaproduc.setModel(tabla);
        
        String arr[]=new String[10];
        
        try {
            
            ps=cnn.prepareStatement("SELECT * FROM productos_venta");
            rs=ps.executeQuery();
            
            while(rs.next()){
                
                arr[0]=rs.getString(1);
                arr[1]=rs.getString(2);
                
                tabla.addRow(arr);

            }
            
        } 
        
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Error en la consulta" + ex);
            
        }

    }
    
    public void listadatos(){
        
        int lista=tablaproduc.getSelectedRow();
        
        if (lista >= 0) {
            
            serie.setSelectedItem(tablaproduc.getValueAt(lista, 0).toString());
            dispo.setSelectedItem(tablaproduc.getValueAt(lista, 1).toString());
            
        }

    }
    
    public void cargarcombo(){
        
        try {
            
            ps=cnn.prepareStatement("SELECT numserie FROM ingreso");
            rs=ps.executeQuery();  
            
            while (rs.next()){
            
                String aux=rs.getString("numserie");
                this.serie.addItem(aux);
            
            }
            
        } 
        
        catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(null, "Error en cargar el combo"+ ex);
            
        }
        
    }
    

